package com.example.korgas;

public class ReadPetrolStationData {

    public String name, contact, address, latitude, longitude, websiteLink, mapLink;
    public String gasolinePrice, dieselPrice, kerosenePrice;
    public String petrolStationPicture;
    public String postSaySomething, postSomethingPicture;

    public String getName() {
        return name;
    }

    public String getContact() {
        return contact;
    }

    public String getAddress() {
        return address;
    }

    public String getLatitude() {
        return latitude;
    }

    public String getLongitude() {
        return longitude;
    }

    public String getWebsiteLink() {
        return websiteLink;
    }

    public String getMapLink() {
        return mapLink;
    }

    public String getGasolinePrice() {
        return gasolinePrice;
    }

    public String getDieselPrice() {
        return dieselPrice;
    }

    public String getKerosenePrice() {
        return kerosenePrice;
    }

    public String getPetrolStationPicture() {
        return petrolStationPicture;
    }

    public String getPostSaySomething() {
        return postSaySomething;
    }

    public String getPostSomethingPicture() {
        return postSomethingPicture;
    }
}
